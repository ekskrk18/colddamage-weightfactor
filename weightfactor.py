def plot_figure_3_pareto_and_timeseries(
    mean_df: pd.DataFrame,
    pareto_df: pd.DataFrame,
    event_tables,
    best_row: pd.Series,
    out_dir: Path,
):
    fig = plt.figure(figsize=(16, 12), constrained_layout=True)
    gs = fig.add_gridspec(3, 2)

    # -------------------------
    # (a) Pareto front
    # -------------------------
    ax = fig.add_subplot(gs[0, :])

    ax.scatter(
        mean_df["S1_peak_dominance"],
        mean_df["S2_concentration"],
        s=24,
        alpha=0.35,
        label="All weight combinations",
    )

    ax.scatter(
        pareto_df["S1_peak_dominance"],
        pareto_df["S2_concentration"],
        s=48,
        alpha=0.9,
        label="Pareto-optimal combinations",
    )

    ax.scatter(
        best_row["S1_peak_dominance"],
        best_row["S2_concentration"],
        s=180,
        marker="*",
        edgecolor="black",
        linewidth=1.0,
        label="Selected optimum",
    )

    ax.set_xlabel("Peak dominance")
    ax.set_ylabel("Concentration")
    ax.set_title("Pareto-based multi-objective optimization")
    ax.grid(True, alpha=0.3)
    ax.legend(fontsize=9)
    add_panel_label(ax, "(a)")

    # -------------------------
    # (b-e) equal vs optimized ACDI
    # -------------------------
    equal_weights = {"w1": 0.25, "w2": 0.25, "w3": 0.25, "w4": 0.25}
    opt_weights = {
        "w1": float(best_row["w1"]),
        "w2": float(best_row["w2"]),
        "w3": float(best_row["w3"]),
        "w4": float(best_row["w4"]),
    }

    panel_labels = ["(b)", "(c)", "(d)", "(e)"]
    axes_ts = [
        fig.add_subplot(gs[1, 0]),
        fig.add_subplot(gs[1, 1]),
        fig.add_subplot(gs[2, 0]),
        fig.add_subplot(gs[2, 1]),
    ]

    event_dict = {ev["event_id"]: ev for ev in event_tables}

    for ax, eid, lab in zip(axes_ts, REPRESENTATIVE_EVENTS, panel_labels):
        if eid not in event_dict:
            ax.axis("off")
            ax.text(0.5, 0.5, f"Event {eid} not found", ha="center", va="center")
            continue

        ev = event_dict[eid]
        dates = ev["factor_df"]["date_plot"]
        acdi_equal = compute_weighted_acdi(ev["factor_df"], equal_weights)
        acdi_opt = compute_weighted_acdi(ev["factor_df"], opt_weights)

        ax.plot(dates, acdi_equal, linewidth=1.4, linestyle="--", label="Equal-weight ACDI")
        ax.plot(dates, acdi_opt, linewidth=1.8, label="Optimized-weight ACDI")
        ax.axvline(ev["event_time"], linestyle="--", linewidth=1.4, label="Event time")
        ax.axhline(0.0, linestyle=":", linewidth=1.0)

        # focus on -10 d to +3 d for visibility
        start = ev["event_time"] - pd.Timedelta(days=10)
        end = ev["event_time"] + pd.Timedelta(days=3)
        ax.set_xlim(start, end)

        ax.set_title(f"Event {eid}")
        ax.set_ylabel("ACDI")
        ax.grid(True, alpha=0.3)
        format_time_axis(ax)
        add_panel_label(ax, lab)

    axes_ts[0].legend(fontsize=8, loc="upper left")

    out_png = out_dir / "Figure_3_pareto_and_optimized_timeseries.png"
    fig.savefig(out_png, dpi=DPI, bbox_inches="tight")
    plt.close(fig)
    print(f"[OK] saved: {out_png}")


