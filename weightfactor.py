def load_smap_event_csv(path: Path):
    df = pd.read_csv(path, encoding="utf-8-sig")

    required = {
        "datetime_kst",
        "event_time_kst",
        "sm_surface",
        "sm_rootzone",
        "surface_temp",
        "soil_temp_layer1",
        "soil_temp_layer2",
    }
    missing = required - set(df.columns)
    if missing:
        raise ValueError(f"{path.name} is missing required columns: {sorted(missing)}")

    df["date"] = pd.to_datetime(df["datetime_kst"], errors="coerce")
    df = df.dropna(subset=["date"]).sort_values("date").reset_index(drop=True)
    df["date_plot"] = df["date"].apply(to_kst_naive)

    event_time = pd.to_datetime(df["event_time_kst"].iloc[0], errors="coerce")
    if pd.isna(event_time):
        raise ValueError(f"Failed to parse event_time_kst in {path.name}")
    event_time = to_kst_naive(event_time)

    temp_cols_k = [
        "surface_temp",
        "soil_temp_layer1",
        "soil_temp_layer2",
        "soil_temp_layer3",
        "soil_temp_layer4",
        "soil_temp_layer5",
        "soil_temp_layer6",
    ]

    for c in temp_cols_k:
        if c in df.columns:
            df[c] = pd.to_numeric(df[c], errors="coerce")
            df[c + "_C"] = df[c] - 273.15

    for c in ["sm_surface", "sm_rootzone"]:
        df[c] = pd.to_numeric(df[c], errors="coerce")

    if "event_id" in df.columns:
        event_id = int(df["event_id"].iloc[0])
    else:
        event_id = infer_event_id_from_filename(path)

    return event_id, event_time, df


