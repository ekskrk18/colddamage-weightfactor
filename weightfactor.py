# 5. FACTOR FUNCTIONS
# =========================================================

def compute_cold_intensity(df: pd.DataFrame, temp_col: str) -> pd.Series:
    steps, min_periods = days_to_steps(df, F1_WINDOW_DAYS)
    cold_deficit = (TBASE_C - df[temp_col]).clip(lower=0)
    return cold_deficit.rolling(steps, min_periods=min_periods).mean()


def compute_rapid_cooling(df: pd.DataFrame, temp_col: str) -> pd.Series:
    steps, min_periods = days_to_steps(df, F2_WINDOW_DAYS)
    dtemp = df[temp_col].diff()
    shock = (-dtemp).clip(lower=0)
    return shock.rolling(steps, min_periods=min_periods).mean()


def compute_vertical_gradient(df: pd.DataFrame, upper_col: str, lower_col: str) -> pd.Series:
    steps, min_periods = days_to_steps(df, F3_WINDOW_DAYS)
    gradient = (df[upper_col] - df[lower_col]).clip(lower=0)
    return gradient.rolling(steps, min_periods=min_periods).mean()


def compute_dryness(df: pd.DataFrame, sm_col: str) -> pd.Series:
    steps, min_periods = days_to_steps(df, F4_WINDOW_DAYS)
    sm_roll = df[sm_col].rolling(steps, min_periods=min_periods).mean()
    sm_ref = sm_roll.quantile(DRY_REF_QUANTILE)
    return (sm_ref - sm_roll).clip(lower=0)


def compute_factor_table(
    df: pd.DataFrame,
    f1_key: str,
    f2_key: str,
    f3_key: str,
    f4_key: str,
) -> pd.DataFrame:
    f1_col = F1_OPTIONS[f1_key]
    f2_col = F2_OPTIONS[f2_key]
    f3_upper_col, f3_lower_col = F3_OPTIONS[f3_key]
    f4_sm_col = F4_OPTIONS[f4_key]

    F1 = compute_cold_intensity(df, f1_col)
    F2 = compute_rapid_cooling(df, f2_col)
    F3 = compute_vertical_gradient(df, f3_upper_col, f3_lower_col)

    dryness = compute_dryness(df, f4_sm_col)
    F4_raw = zscore_series(F1) * zscore_series(dryness)

    out = pd.DataFrame({
        "date_plot": df["date_plot"],
        "Z_F1": zscore_series(F1),
        "Z_F2": zscore_series(F2),
        "Z_F3": zscore_series(F3),
        "Z_F4": zscore_series(F4_raw),
    })

    return out


def compute_weighted_acdi(factor_df: pd.DataFrame, weights: dict) -> pd.Series:
    return (
        weights["w1"] * factor_df["Z_F1"]
        + weights["w2"] * factor_df["Z_F2"]
        + weights["w3"] * factor_df["Z_F3"]
        + weights["w4"] * factor_df["Z_F4"]
    )


# =========================================================
# 6. METRICS
# =========================================================

def compute_scores(acdi: pd.Series, dates: pd.Series, event_time: pd.Timestamp):
    event_start = event_time - pd.Timedelta(hours=EVENT_WINDOW_HOURS)

    dates = pd.Series(dates).apply(to_kst_naive)
    event_time = to_kst_naive(event_time)

    mask_event = (dates >= event_start) & (dates <= event_time)
    mask_early = dates < event_start
    mask_pre = dates < event_time

    event_vals = pd.Series(acdi[mask_event]).dropna()
    early_vals = pd.Series(acdi[mask_early]).dropna()
    pre_vals = pd.Series(acdi[mask_pre]).dropna()

    if len(event_vals) == 0 or len(pre_vals) == 0:
        return np.nan, np.nan, np.nan

    sigma = float(np.nanstd(pre_vals))
    if pd.isna(sigma) or sigma == 0:
        sigma = 1e-6

    # S1: peak dominance
    p_event = safe_nanmax(event_vals)
    p_early = safe_nanmax(early_vals)
    S1 = (p_event - p_early) / sigma

    # S2: concentration
    m_event = safe_nanmean(event_vals)
    m_early = safe_nanmean(early_vals)
    S2 = (m_event - m_early) / sigma

    # S3: exceedance duration
    if THRESHOLD_MODE.lower() == "zero":
        threshold = 0.0
    elif THRESHOLD_MODE.lower() == "quantile":
        threshold = float(np.nanquantile(pre_vals, THRESHOLD_Q))
    else:
        raise ValueError("THRESHOLD_MODE must be either 'zero' or 'quantile'.")

    # Unit: number of data points within the 48-h event window.
    # With 4 observations/day, this is approximately 6-hour units.
    S3 = float(np.sum(event_vals > threshold))

    return S1, S2, S3


