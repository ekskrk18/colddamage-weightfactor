def infer_event_id_from_filename(path: Path) -> int:
    m = re.search(r"event_(\d+)", path.name)
    if not m:
        raise ValueError(f"Cannot parse event id from file name: {path.name}")
    return int(m.group(1))


def to_kst_naive(ts) -> pd.Timestamp:
    ts = pd.Timestamp(ts)
    if ts.tzinfo is not None:
        ts = ts.tz_convert("Asia/Seoul").tz_localize(None)
    return ts


def zscore_series(s: pd.Series) -> pd.Series:
    s = pd.to_numeric(s, errors="coerce")
    mu = s.mean(skipna=True)
    sd = s.std(skipna=True, ddof=0)

    if pd.isna(sd) or sd == 0:
        return pd.Series(np.zeros(len(s)), index=s.index, dtype=float)

    return (s - mu) / sd


def estimate_steps_per_day(df: pd.DataFrame) -> int:
    dt = df["date_plot"].sort_values().diff().dropna()
    if len(dt) == 0:
        return 4

    median_hours = dt.dt.total_seconds().median() / 3600.0
    if median_hours <= 0 or pd.isna(median_hours):
        return 4

    return max(1, int(round(24.0 / median_hours)))


def days_to_steps(df: pd.DataFrame, days: int):
    steps_per_day = estimate_steps_per_day(df)
    window_steps = max(1, int(days * steps_per_day))
    min_periods = max(1, int(np.ceil(window_steps * MIN_VALID_RATIO)))
    return window_steps, min_periods


def safe_nanmax(x):
    x = pd.Series(x).dropna()
    return np.nan if len(x) == 0 else float(np.nanmax(x))


def safe_nanmean(x):
    x = pd.Series(x).dropna()
    return np.nan if len(x) == 0 else float(np.nanmean(x))


def normalize_0_1(s: pd.Series) -> pd.Series:
    s = pd.to_numeric(s, errors="coerce")
    mn, mx = s.min(skipna=True), s.max(skipna=True)

    if pd.isna(mn) or pd.isna(mx) or mx == mn:
        return pd.Series(np.zeros(len(s)), index=s.index, dtype=float)

    return (s - mn) / (mx - mn)


