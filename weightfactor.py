def generate_simplex_weights(step: float) -> pd.DataFrame:
    values = np.round(np.arange(0, 1 + step / 2, step), 10)

    rows = []
    for w1 in values:
        for w2 in values:
            for w3 in values:
                w4 = 1.0 - w1 - w2 - w3
                if w4 < -1e-9:
                    continue
                if w4 > 1 + 1e-9:
                    continue
                w4 = round(w4, 10)
                if abs((w1 + w2 + w3 + w4) - 1.0) <= 1e-9:
                    rows.append({"w1": w1, "w2": w2, "w3": w3, "w4": w4})

    return pd.DataFrame(rows)


def build_all_event_factor_tables(files):
    f1_key, f2_key, f3_key, f4_key = SELECTED_CASE

    event_tables = []
    for path in files:
        event_id, event_time, df = load_smap_event_csv(path)
        factor_df = compute_factor_table(df, f1_key, f2_key, f3_key, f4_key)

        event_tables.append({
            "event_id": event_id,
            "event_time": event_time,
            "dates": df["date_plot"].copy(),
            "factor_df": factor_df,
        })

    return event_tables


def evaluate_weight_grid(event_tables, weight_df):
    rows = []

    for _, wr in weight_df.iterrows():
        weights = {
            "w1": float(wr["w1"]),
            "w2": float(wr["w2"]),
            "w3": float(wr["w3"]),
            "w4": float(wr["w4"]),
        }

        for ev in event_tables:
            acdi = compute_weighted_acdi(ev["factor_df"], weights)
            S1, S2, S3 = compute_scores(acdi, ev["dates"], ev["event_time"])

            rows.append({
                "event": ev["event_id"],
                **weights,
                "S1_peak_dominance": S1,
                "S2_concentration": S2,
                "S3_exceedance_duration": S3,
            })

    return pd.DataFrame(rows)


def add_integrated_score(mean_df: pd.DataFrame) -> pd.DataFrame:
    out = mean_df.copy()

    metric_cols = list(METRIC_WEIGHTS.keys())
    for col in metric_cols:
        out[col + "_norm"] = normalize_0_1(out[col])

    total_weight = sum(METRIC_WEIGHTS.values())
    out["Integrated_score"] = 0.0

    for col, w in METRIC_WEIGHTS.items():
        out["Integrated_score"] += (w / total_weight) * out[col + "_norm"]

    return out


def summarize_weight_results(event_metric_df: pd.DataFrame):
    metric_cols = [
        "S1_peak_dominance",
        "S2_concentration",
        "S3_exceedance_duration",
    ]

    mean_df = (
        event_metric_df
        .groupby(["w1", "w2", "w3", "w4"], as_index=False)[metric_cols]
        .mean()
    )

    mean_df = add_integrated_score(mean_df)
    mean_df = mean_df.sort_values("Integrated_score", ascending=False).reset_index(drop=True)

    return mean_df


def find_pareto_front(df: pd.DataFrame, objective_cols):
    values = df[objective_cols].to_numpy(dtype=float)

    # Maximize all objectives.
    is_pareto = np.ones(values.shape[0], dtype=bool)

    for i, v in enumerate(values):
        if not is_pareto[i]:
            continue

        dominates_i = np.all(values >= v, axis=1) & np.any(values > v, axis=1)
        if np.any(dominates_i):
            is_pareto[i] = False

    return df.loc[is_pareto].copy().sort_values("Integrated_score", ascending=False)


