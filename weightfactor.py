def add_panel_label(ax, label):
    ax.text(
        0.01, 0.98, label,
        transform=ax.transAxes,
        ha="left", va="top",
        fontsize=12,
        fontweight="bold",
        bbox=dict(boxstyle="round,pad=0.2", facecolor="white", alpha=0.8, edgecolor="none"),
    )


def format_time_axis(ax):
    locator = mdates.AutoDateLocator(minticks=4, maxticks=7)
    formatter = mdates.DateFormatter("%Y-%m-%d")
    ax.xaxis.set_major_locator(locator)
    ax.xaxis.set_major_formatter(formatter)
    plt.setp(ax.get_xticklabels(), rotation=30, ha="right")


def plot_heatmap_matrix(ax, mat, title, cbar_label):
    im = ax.imshow(mat.values, origin="lower", aspect="auto")

    ax.set_title(title)
    ax.set_xlabel("Weight of F3")
    ax.set_ylabel("Weight of F4")

    ax.set_xticks(np.arange(len(mat.columns)))
    ax.set_xticklabels([f"{v:.2f}" for v in mat.columns], rotation=45, ha="right", fontsize=8)

    ax.set_yticks(np.arange(len(mat.index)))
    ax.set_yticklabels([f"{v:.2f}" for v in mat.index], fontsize=8)

    cbar = plt.colorbar(im, ax=ax)
    cbar.set_label(cbar_label)

    return im


