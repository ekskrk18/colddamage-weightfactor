"""
ACDI Weight Optimization for Agricultural Cold Damage

Purpose
-------
This script extends the existing equal-weight ACDI framework to a weighted ACDI:

    ACDI_w = w1*Z(F1) + w2*Z(F2) + w3*Z(F3) + w4*Z(F4)
    subject to w1 + w2 + w3 + w4 = 1

It generates exactly three manuscript-style figure files:

    Figure 1. Sensitivity analysis of factor weights
        (a) Integrated score sensitivity curves
        (b) Metric-specific sensitivity heatmap

    Figure 2. Response surface analysis
        (a) Integrated performance surface for w3 and w4
        (b) Metric-specific response surface summary

    Figure 3. Pareto optimization and optimized ACDI comparison
        (a) Pareto front
        (b-e) Equal-weight vs optimized-weight ACDI for representative events

Input
-----
CSV files generated by the SMAP download/extraction script:
    event_*_SMAP_L4_SPL4SMGP*_30d_to_3d_4perday_sm_temp_layers.csv

Required columns:
    datetime_kst, event_time_kst,
    sm_surface, sm_rootzone,
    surface_temp, soil_temp_layer1, soil_temp_layer2

Author note
-----------
The factor definitions and event scoring structure follow the user's previous ACDI scripts.
"""

