def main():
    files = sorted(SMAP_DIR.glob(SMAP_PATTERN))
    if not files:
        raise FileNotFoundError(
            "SMAP event CSV files were not found.\n"
            f"SMAP_DIR = {SMAP_DIR}\n"
            f"SMAP_PATTERN = {SMAP_PATTERN}"
        )

    print(f"[INFO] Found {len(files)} SMAP event files")
    print(f"[INFO] Selected ACDI case: F1={SELECTED_CASE[0]}, F2={SELECTED_CASE[1]}, F3={SELECTED_CASE[2]}, F4={SELECTED_CASE[3]}")
    print(f"[INFO] Weight step: {WEIGHT_STEP}")

    # -----------------------------------------------------
    # Build factor tables once, then reuse them for all weights
    # -----------------------------------------------------
    event_tables = build_all_event_factor_tables(files)
    print(f"[INFO] Built factor tables for {len(event_tables)} events")

    # -----------------------------------------------------
    # Full simplex weight search
    # -----------------------------------------------------
    weight_df = generate_simplex_weights(WEIGHT_STEP)
    weight_df.to_csv(OUT_DIR / "weight_grid.csv", index=False, encoding="utf-8-sig")
    print(f"[INFO] Weight combinations: {len(weight_df)}")

    event_metric_df = evaluate_weight_grid(event_tables, weight_df)
    event_metric_df.to_csv(
        OUT_DIR / "weight_grid_event_metrics.csv",
        index=False,
        encoding="utf-8-sig",
    )

    mean_df = summarize_weight_results(event_metric_df)
    mean_df.to_csv(
        OUT_DIR / "weight_grid_mean_metrics.csv",
        index=False,
        encoding="utf-8-sig",
    )

    best_row = mean_df.iloc[0]
    pd.DataFrame([best_row]).to_csv(
        OUT_DIR / "selected_optimized_weight.csv",
        index=False,
        encoding="utf-8-sig",
    )

    print("[RESULT] Selected optimized weights")
    print(f"  w1 = {best_row['w1']:.2f}  ({FACTOR_LABELS['w1']})")
    print(f"  w2 = {best_row['w2']:.2f}  ({FACTOR_LABELS['w2']})")
    print(f"  w3 = {best_row['w3']:.2f}  ({FACTOR_LABELS['w3']})")
    print(f"  w4 = {best_row['w4']:.2f}  ({FACTOR_LABELS['w4']})")
    print(f"  Integrated score = {best_row['Integrated_score']:.4f}")

    # -----------------------------------------------------
    # Pareto front
    # -----------------------------------------------------
    objective_cols = [
        "S1_peak_dominance",
        "S2_concentration",
        "S3_exceedance_duration",
    ]
    pareto_df = find_pareto_front(mean_df, objective_cols)
    pareto_df.to_csv(OUT_DIR / "pareto_optimal_weights.csv", index=False, encoding="utf-8-sig")
    print(f"[INFO] Pareto solutions: {len(pareto_df)}")

    # -----------------------------------------------------
    # Sensitivity table
    # -----------------------------------------------------
    sens_df = build_sensitivity_table(event_tables)
    sens_df.to_csv(OUT_DIR / "sensitivity_analysis_results.csv", index=False, encoding="utf-8-sig")

    # -----------------------------------------------------
    # Response surface
    # -----------------------------------------------------
    surface_df = build_response_surface(mean_df)
    surface_df.to_csv(OUT_DIR / "response_surface_w3_w4.csv", index=False, encoding="utf-8-sig")

    # -----------------------------------------------------
    # Figures: exactly three figure files
    # -----------------------------------------------------
    plot_figure_1_sensitivity(sens_df, OUT_DIR)
    plot_figure_2_response_surface(surface_df, best_row, OUT_DIR)
    plot_figure_3_pareto_and_timeseries(mean_df, pareto_df, event_tables, best_row, OUT_DIR)

    print("\n[DONE] ACDI weight optimization completed.")
    print(f"[DONE] Results and figures saved in:\n  {OUT_DIR}")


if __name__ == "__main__":
    main()
