# 8. SENSITIVITY ANALYSIS
# =========================================================

def build_sensitivity_table(event_tables):
    rows = []
    values = np.round(np.arange(0, 1.0 + WEIGHT_STEP / 2, WEIGHT_STEP), 10)

    for target in ["w1", "w2", "w3", "w4"]:
        others = [w for w in ["w1", "w2", "w3", "w4"] if w != target]

        for target_value in values:
            remaining = 1.0 - target_value
            weights = {target: float(target_value)}
            for o in others:
                weights[o] = float(remaining / 3.0)

            event_rows = []
            for ev in event_tables:
                acdi = compute_weighted_acdi(ev["factor_df"], weights)
                S1, S2, S3 = compute_scores(acdi, ev["dates"], ev["event_time"])
                event_rows.append({
                    "S1_peak_dominance": S1,
                    "S2_concentration": S2,
                    "S3_exceedance_duration": S3,
                })

            tmp = pd.DataFrame(event_rows)
            mean_values = tmp.mean(numeric_only=True).to_dict()

            rows.append({
                "target_factor": target,
                "target_weight": target_value,
                **weights,
                **mean_values,
            })

    sens_df = pd.DataFrame(rows)

    # Normalize within the sensitivity table so that integrated score is comparable.
    sens_df = add_integrated_score(sens_df)

    return sens_df


# =========================================================
# 9. RESPONSE SURFACE
# =========================================================

def build_response_surface(mean_df: pd.DataFrame):
    """
    Summarize performance over w3 and w4.
    If several w1-w2 splits exist for the same w3-w4 pair,
    keep the maximum integrated score.
    """
    idx = (
        mean_df
        .sort_values("Integrated_score", ascending=False)
        .groupby(["w3", "w4"], as_index=False)
        .head(1)
        .reset_index(drop=True)
    )
    return idx


def pivot_surface(surface_df: pd.DataFrame, value_col: str):
    mat = surface_df.pivot_table(
        index="w4",
        columns="w3",
        values=value_col,
        aggfunc="max",
    )
    mat = mat.sort_index(ascending=True)
    mat = mat.reindex(sorted(mat.columns), axis=1)
    return mat


