def plot_figure_1_sensitivity(sens_df: pd.DataFrame, out_dir: Path):
    fig, axes = plt.subplots(1, 2, figsize=(15, 6), constrained_layout=True)

    ax = axes[0]
    for factor in ["w1", "w2", "w3", "w4"]:
        sub = sens_df[sens_df["target_factor"] == factor].sort_values("target_weight")
        ax.plot(
            sub["target_weight"],
            sub["Integrated_score"],
            marker="o",
            linewidth=1.8,
            label=FACTOR_LABELS[factor],
        )

    ax.set_xlabel("Assigned weight to target factor")
    ax.set_ylabel("Integrated performance score")
    ax.set_title("One-factor-at-a-time sensitivity")
    ax.grid(True, alpha=0.3)
    ax.legend(fontsize=9)
    add_panel_label(ax, "(a)")

    # Metric-specific heatmap:
    # For each factor, compute the performance range caused by varying that factor.
    heat_rows = []
    for factor in ["w1", "w2", "w3", "w4"]:
        sub = sens_df[sens_df["target_factor"] == factor]
        row = {"Factor": FACTOR_LABELS[factor]}
        for metric in ["S1_peak_dominance", "S2_concentration", "S3_exceedance_duration", "Integrated_score"]:
            row[metric] = sub[metric].max() - sub[metric].min()
        heat_rows.append(row)

    heat_df = pd.DataFrame(heat_rows).set_index("Factor")
    heat_df_norm = heat_df.copy()
    for c in heat_df_norm.columns:
        heat_df_norm[c] = normalize_0_1(heat_df_norm[c])

    ax = axes[1]
    im = ax.imshow(heat_df_norm.values, aspect="auto")
    ax.set_title("Relative sensitivity by metric")
    ax.set_xticks(np.arange(len(heat_df_norm.columns)))
    ax.set_xticklabels([METRIC_LABELS.get(c, c) for c in heat_df_norm.columns], rotation=35, ha="right")
    ax.set_yticks(np.arange(len(heat_df_norm.index)))
    ax.set_yticklabels(heat_df_norm.index)

    for i in range(heat_df_norm.shape[0]):
        for j in range(heat_df_norm.shape[1]):
            ax.text(j, i, f"{heat_df_norm.iloc[i, j]:.2f}", ha="center", va="center", fontsize=9)

    cbar = fig.colorbar(im, ax=ax)
    cbar.set_label("Normalized sensitivity range")
    add_panel_label(ax, "(b)")

    out_png = out_dir / "Figure_1_sensitivity_analysis.png"
    fig.savefig(out_png, dpi=DPI, bbox_inches="tight")
    plt.close(fig)
    print(f"[OK] saved: {out_png}")


